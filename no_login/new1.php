<h3>Hãng hàng không Bamboo Airways đổi lịch chuyến bay của tôi</h3>
<h5>
    Chúng tôi rất tiếc vì việc thay đổi lịch trình bay ảnh hưởng đến kế hoạch của bạn. Nếu chuyến bay của bạn bị hãng hàng không đổi lịch, bạn có thể tham khảo các chính sách sau:
</h5>
<h1>A. Nếu chuyến bay của bạn bị đổi lịch sớm ít hơn 1 tiếng hoặc muộn ít hơn 3 tiếng so với giờ khởi hành ban đầu:</h1>
<li>
Theo chính sách mới nhất từ hãng hàng không Bamboo Airways, việc đổi lịch bay sớm ít hơn 1 tiếng hoặc muộn ít hơn 3 tiếng so với giờ khởi hành gốc được xem là điều chỉnh lịch không đáng kể. Do vậy, chính sách vé thông thường vẫn sẽ được áp dụng. Vì vậy, chúng tôi khuyến khích bạn thực hiện hành trình bay mới từ hãng hàng không. Nếu bạn có nhu cầu hoàn vé, bạn có thể tham khảo hướng dẫn sau:
</li>
<ul>Lưu ý
    <li>Bạn sẽ vẫn cần chọn “Tự hủy” làm lý do hoàn vé vì như đã đề cập ở trên, hãng hàng không xem xét trường hợp này là thay đổi lịch không đáng kể và không có chính sách hỗ trợ đặc biệt.</li>
    <li>Yêu cầu hoàn trước/ trong khung 3 tiếng so với giờ bay, hoặc sau giờ bay với mức phí hoàn khác nhau. Bạn nên gửi yêu cầu hoàn trước ít nhất 03 tiếng so với giờ bay để tránh tăng phí hoàn từ hãng hàng không.</li>
    <li>Thường xuyên theo dõi cập nhật tình trạng chuyến bay từ hãng và Traveloka (qua thông báo đẩy, email) để được hỗ trợ kịp thời. </li>
    <li>Nếu lý do hoàn vé chưa hợp lệ, Traveloka sẽ căn cứ theo chính sách của hãng bay để cập nhật đúng lý do và gửi đến hãng.</li>
    <li>Kết quả hoàn vé hoàn toàn phụ thuộc vào quyết định từ hãng bay.</li>
    <li>Khi đã xác nhận và đồng ý hoàn, yêu cầu hoàn sẽ không thể huỷ bỏ, thời gian hoàn có thể kéo dài lên đến 90 ngày làm việc (không tính thứ Bảy, Chủ Nhật và ngày lễ) kể từ ngày ghi nhận đủ thông tin. Vui lòng tham khảo tiến trình hoàn tại </li>
    <li>Nếu mã đặt chỗ không được hoàn hủy, Traveloka rất tiếc chưa thể hỗ trợ bạn trong trường hợp này vì đây là chính sách từ hãng hàng không. Do đó, bạn vui lòng cân nhắc sử dụng vé. </li>
</ul>
